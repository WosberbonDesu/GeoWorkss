import 'package:flutter/material.dart';
import 'package:google_maps_flutter/google_maps_flutter.dart';



class GoogleMapScreen extends StatefulWidget {
  const GoogleMapScreen({Key? key}) : super(key: key);

  @override
  _GoogleMapScreenState createState() => _GoogleMapScreenState();
}

class _GoogleMapScreenState extends State<GoogleMapScreen> {
  Set<Marker> _markers = {};
  BitmapDescriptor? mapMarker;
  @override
  //void initState(){
  //super.initState();
//setCustomMarker();
  //}

  //void setCustomMarker()async{
  //mapMarker = await BitmapDescriptor.defaultMarker;
  //}

  void _onMapCreated(GoogleMapController controller){
    controller.setMapStyle(Utils.mapStyle);
    setState(() {
      _markers.add(
        Marker(
            markerId:
            MarkerId("id-1"),
            position: LatLng(39.766705,30.525631),

          infoWindow: InfoWindow(
            title: "Eskisehir Merkez",
            snippet: "Berbat Bir yer",

          )

        )
      );

    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Padding(
          padding: const EdgeInsets.symmetric(horizontal: 66.0),
          child: Text("Google Maps"),
        ),
      ),
      body: Center(
        child: GoogleMap(

          onMapCreated: _onMapCreated,
            markers: _markers,
            initialCameraPosition: CameraPosition(
            target: LatLng(39.766705,30.525631),
        zoom: 15,
            )
        )
      ),
    );
  }
}



